  print("Solving equantion #1")
  print("--------------------------------------------")
  A = int(input("Please enter your value of a (coefficient of x^2): "))
  B = int(input("Please enter your value of b (coefficient of x): "))
  C = int(input("Please enter your value of c (coefficient): "))

  print(f"The solutions to {A}x^2 + {B}x + {C}=0 :")
  if ((B**2)-4*A*C) < 0:
   print("La solución de la ecuación es con números complejos")
  else:
   x1 = (-B+sqrt(B**2-(4*A*C)))/(2*A)
   x2 = (-B-sqrt(B**2-(4*A*C)))/(2*A)
   print("Las soluciones de la ecuación son:")
   print(f"x1=({x1})")
   print(f"x2=({x2})")
   print("Thank you for using the Quadratic Equation Solver App. Goobye")


